package com.servletStore.settings.standard.model;

public class StandardPOJO {

		private int id;
		private String name;
		
		private int stdId;
		private int schoolSectionId;
		
		private int schoolId;
		private int sectionId;
		
		
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getName() {
			return name;
		}
		public void setName(String name) {
			this.name = name;
		}
		
		
		public int getStdId() {
			return stdId;
		}
		public void setStdId(int stdId) {
			this.stdId = stdId;
		}
		public int getSchoolSectionId() {
			return schoolSectionId;
		}
		public void setSchoolSectionId(int schoolSectionId) {
			this.schoolSectionId = schoolSectionId;
		}
		public int getSchoolId() {
			return schoolId;
		}
		public void setSchoolId(int schoolId) {
			this.schoolId = schoolId;
		}
		public int getSectionId() {
			return sectionId;
		}
		public void setSectionId(int sectionId) {
			this.sectionId = sectionId;
		}
		
		
		
		
}
